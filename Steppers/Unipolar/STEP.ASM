ORG 0000H
SJMP 0030H



ORG 0030H




START:		MOV P2,#0FFH
		
LOOP:		MOV P2,#01H
		ACALL DELAY
		MOV P2,#02H
		ACALL DELAY
		MOV P2,#04H
		ACALL DELAY
		MOV P2,#08H
		ACALL DELAY
		SJMP LOOP


DELAY: 		MOV R6,#10H
		MOV R5,#0FFH	
		DJNZ R5,$
		DJNZ R6,$-2
		RET

END
	